// Helper function to get available languages for the selector
export const getAvailableLanguages = () => {
  return Object.keys(languages).map(key => ({
    code: key,
    name: languages[key].name,
    flag: languages[key].flag
  }));
};

// Helper function to get translations for a specific language
export const getTranslations = (languageCode) => {
  return languages[languageCode]?.translations || languages.en.translations;
};

